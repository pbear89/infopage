	            <p class="font-weight mdb-text py-2">
	            	 La Universidad Nacional Experimental Simón Rodríguez (Unesr) ubicada en Santa Fe Norte, se convirtió en sala de sesiones de la Comisión Permanente de Educación, Ciencia y Tecnología de la Asamblea Nacional Constituyente presidida por la constituyente Haydeé Huérfano y en la vicepresidencia por el constituyentista Ricardo Sánchez.
	            </p>		
				<p class="font-weight mdb-text py-2">
					En esta reunión, que contó con el quórum requerido para su realización, empezó con las palabras de bienvenida del rector de esta casa de estudio PhD. Enrique Ramos, quien expresó “Según las instrucciones recibidas del Ministro de Educación Universitaria, Ciencia y Tecnología, Hugbel Roa, les damos la bienvenida y les extendemos un abrazo solidario desde la primera Universidad que recibe la visita de la Comisión en sus espacios (...) Hoy, nuestros 22 Núcleos, 17 Extensiones y 4 estaciones Experimentales se declaran en tiempos de Constituyente Educativa. Desde acá nos comprometemos a cumplir las tareas que se nos encomienden desde esta Comisión y a hacerles llegar propuestas claras para la redacción de la que será la nueva Constitución de la República Bolivariana de Venezuela, les colocamos a su disposición la participación de nuestros 166 mil estudiantes, sus facilitadores, investigadores, directivos y trabajadores de la Unesr”, afirmó el también constituyentista Ramos.


				</p>		
				<p class="font-weight mdb-text py-2">

					Continuo la reunión, según lo establecido en la propuesta del día, que incluyó la presentación de las nuevas autoridades de la Universidad Nacional Experimental Simón Rodríguez y la entrega de 10 propuestas por parte de la Federación de Centros de Estudiantes en la persona de su Presidente, Kennedy Morales que amerita, según lo expresó “... más que una toma de decisiones una apertura al diálogo en nuestra casa de estudios y más allá de la Universidad Simón Rodríguez, así es como nosotros logramos los acuerdos, sobre la base del debate”.
				</p>		
				<p class="font-weight mdb-text py-2">

					El vicepresidente de la Comisión, Ricardo Sánchez, calificó como inédita la participación de los estudiantes ante un espacio de este tipo como lo es una Comisión de la Asamblea Nacional Constituyente Venezolana. “En este despliegue de la Constituyente Educativa, siguiendo las instrucciones de nuestro Presidente Nicolás Maduro Moros y de nuestro ministro Hugbel Roa Carucí, el hecho de que un constituyente como Enrique Ramos asuma la batuta desde esta universidad se presta para anunciar mejores tiempos de lo que pueda significar la construcción del hecho educativo, la búsqueda de la verdad y de la pertinencia que se busca para beneficio del país”
				</p>		
				<p class="font-weight mdb-text py-2">

					Asimismo destacó Sánchez “... la universidad que nosotros visualizamos es aquella que este comprometida con la causa y las luchas populares; una universidad en la que en sus Programas de Formación, sus escuelas, extensiones, su docencia, su investigación tengan pertinencia para avanzar hacia el modelo de la Venezuela potencia (...) Hoy, la Asamblea Nacional Constituyente recibe al Movimiento Estudiantil porque estamos en tiempos de revolución de la mano de un hombre como el Presidente Nicolás Maduro, que entiende esas filosofías de que las nuevas generaciones tienen mucho para aportar. Esta generación de jóvenes que se queda en Venezuela, que no es espectadora de la crisis sino protagonista de los cambios que deberemos asumir de cara a lo que significa la transición del Modelo rentista petrolero al Modelo de la Venezuela Potencia lo que contrasta con lo que está ocurriendo en otros países de América Latina”. 
				</p>		
				<p class="font-weight mdb-text py-2">
					Destacó el vicepresidente de la Comisión “Aquí estamos nosotros, los que luchamos por la universidad que queremos, los que queremos a la universidad, gracias a este planteamiento de las puertas abiertas de la Unesr estamos seguros que lograremos el despliegue necesario de los constituyentes en sus sectores y en el territorio que fue la orientación que nos dio el Presidente Nicolás Maduro en este renacer constituyente”.
				</p>			
				<p class="font-weight mdb-text py-2">
					Las autoridades ueserristas, su rector Ramos y la vicerrectora administrativa, Ana Endrina Gómez quisieron subordinarse ante el poder máximo plenipotenciario que representa la ANC y venir a cumplir el trabajo rectoral en la Unesr. “He asumido una serie de compromisos y entre ellos está el colocar a disposición de la Comisión de Educación, Ciencia y Tecnología de la ANC, todas nuestras sedes, núcleos, extensiones, estaciones experimentales e institutos de investigación para la realización de un trabajo que refleje una primera tarea de colocar la universidad donde la requiera el país para paliar, desde sus espacios, esta guerra económica inducida que estamos padeciendo y convertir la patria en un país potencia, en la Venezuela Potencia (...) Otro compromiso es abrirle un espacio a los participantes de la universidad, sus facilitadores, trabajadores”, expresó el rector Ramos.

				</p>				
				<p class="font-weight mdb-text py-2">
					
					Finalizó la máxima autoridad de la Unesr afirmando: "Asumimos la tarea clara que nos dejó el Ministro Hugbel Roa, que la Unesr de un giro y mire hacia la Venezuela que está en crisis para que, con el trabajo de todos, logremos llevarla hasta lo productivo, a través de los proyectos científicos y las líneas de investigación y que llevemos a Venezuela, en corto tiempo, hasta la prosperidad económica".
				</p>		

				
