 	<h4 class="text-left font-bold ">Estación Experimental "Santa Ana"</h4>
	
    <p class="text-justify"> 
        Situada en la comunidad rural del Santa Ana, en el tramo de la carretera Canoabo-Urama, parroquia Canoabo, municipio Bejuma, estado Carabobo; y con poco mas de 100 hectareas, existen proyectos de citricas, piñas, parchitas, lombricultura, nim y vetiver.
    </p>
    <hr>

 	<h4 class="text-left font-bold ">Estación Experimental "Cataurito"</h4>
	
    <p class="text-justify"> 
        Situada en el cerro Cataurón, parte alta de la zona de Tucutunemo, municipio Zamora (Villa de Cura), estado Aragua; a unos 1.000m.s.n.m. .
        Acá de desarrollan proyectos en aves ponedoras, donde se ha logrado la producción de estirpes de alto rendimiento, ademas de la comercialización de pollitas, embriones y huevos de consumo. Junto a esto, hay un área sembrada de café asociado con pino Caribe y en ejecución proyectos de lombricultira y hortalizas.
    </p>
    <hr>
    
 	<h4 class="text-left font-bold ">Estación Experimental "Río Negro"</h4>
	
    <p class="text-justify"> 
        Situada en el poblado de Río Negro, Parroquia Capaya, municipio Acevedo (Caucagüa), estado Bolivariano de Miranda, sub-región Barlovento. Son aproximadamente unas 600 hectáreas, con características de bosque húmedo tropical; y se adelantan proyectos de citrica, parchita, hortalizas y ganadería doble proposito.
    </p>
    <hr>

 	<h4 class="text-left font-bold ">Estación Experimental "La Iguana"</h4>
	
    <p class="text-justify"> 
        Situada a noventa (90) kilómetros de la población de Santa María de Ipire, al sur del estádo Guárico. Tiene aproximadamente unas 3.000 hectáreas y en ella de desarrollan proyectos de investigación en las áreas de agro-ecología y ganadería doble-proposito.
    </p>
