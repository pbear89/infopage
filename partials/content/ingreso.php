	<h4 class="text-center">Formas de Ingreso</h4>
		<hr>
		<p class="text-justify"> 
			
<br>
			El reglamento sobre el Régimen de Estudios en el Capitulo III, Sección primera, en sus Artículos 52 al 55 define el sistema de selección, ingreso y permanencia de los participantes a la Universidad, de manera que las políticas contribuyan a facilitar el alcance de los objetivos institucionales.

		</p>
		<p class="text-justify"> 
					
		       La Universidad, a nivel de Pregrado mantendrá las siguientes formas de ingreso:
		</p>
		<p class="text-justify"> 
			<ul>
				<li>1.- Sistema Nacional de Ingresos. </li>
				<li>2.- Equivalencia de Estudios. </li>
				<li>3.- Acreditación del Aprendizaje por Experiencia. </li>
				<li>4.- Otras formas de ingreso que sean aprobadas por la autoridad competente. </li>
				<li>5.- Maestros en Servicios. </li>
				<li>6.- Convenios Gremiales. </li>
			</ul>
		</p>
		<p class="text-justify"> 
			Para ser más específicos podrán ingresar a la Universidad Nacional Experimental Simón Rodríguez:
		</p>
		<p class="text-justify"> 
			<strong>A)</strong> Los aspirantes asignados por el Registro Único del Sistema Nacional de Ingreso a la Educación Superior de estudiantes.
		 <br>
			<strong>B)</strong> Las asignados por la UNESR (API) que representen y superen la prueba interna (incluye ingresos por equivalencia y acreditación, previo dictamen y maestros en servicio) esta no incluye actualmente en la UNESR.
		 <br>
			<strong>C)</strong> Aspirantes atletas de alta competencia.
		 <br>
			<strong>D)</strong> Aspirantes amparados por convenios gremiales.
		 <br>
		</p>
		<p class="text-justify"> 
		Según las Normas de Selección y Admisión de estudiantes de pregrado la selección de bachilleres se realiza por el método del cupo diferencial.

			
