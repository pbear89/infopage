		<hr>

		<p class="text-justify"> 
			El valor de compra del petro en divisa internacional tendrá un descuento del 3% para las primeras tres emisiones. El valor de liquidación del petro en cualquier divisa internacional está sujeto a una tasa administrativa del 5%. Se establece el plan de ahorro en petro denominado Stakin, con el cual los usuarios prestaran apoyo a la red y su dinámica.
			<br>
			Este plan consiste en la emisión de recompensas pre definidas de acuerdo a la inmovilidad de los petros en las Wallet de los usuarios. De esta manera, un usuario que posea una cantidad cualquiera, al no hacer uso de los mismos y conservarlos en su Wallet por un tiempo determinado, al culminar este lapso, recibirá una recompensa en petros, según los parámetros establecidos por SUNACRIP y la TCV.
			<br>
			Estos lapsos sobre la inmovilidad y el porcentaje de la recompensa serán variables y definidos de acuerdo a la misma dinámica del mercado de compra y emision de los petros. Esto quiere decir que se efectuara minería de petro, sino  que tales recompensas derivaran de las transacciones en la BlockChain.
			
