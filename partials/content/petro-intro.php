	<h4 class="text-center">Introducción al Petro </h4>
		<hr>

		<p class="text-justify"> 
			La economia, la tecnología y el intercambio de la información, constituyen en la actualidad las nuevas formas de esquemas y estructuras hegemónicas que no solo establecen maneras predefinidas de intercambio de mercancias y riquezas, sino también de imposición  de conocimiento para el intercambio, así como de información predefinida. Todo esto genera la construcción de esquemas de pensamientos basados en intereses particulares, lo cual tiene como fin el aumento de la dominacion inicial.
			
            Dentro de este contexto, las criptomonedas representan la manera como el mundo busca expandir sus intercambios económicos y financieros, mas allá de la estructura hegemónica del sistema financiero tradicional.

            El petro es la primera moneda digital promovida y emitida por el estado, respaldada por las riquezas naturales, lo que convierte a Venezuela en el primer país pionero y a la vanguardia de esta nueva revolución económica mundial. Todo esto permite que el Petro tenga una salida y fuerte naturaleza competitiva frente a los mercados financieros internacionales; representando además, transparencia, inclusión y justicia mundial.
            <br>
            <br>
			Las estructuras económicas y financieras a las que se ha sometido el mundo, requieren una respuesta contundente visionaria y revolucionaria, que permita compensar las distorsiones por estas estructuras. Entre estas respuestas, las estrategias deben apuntar a la generacion de recursos y a la movilidad de los mismos como parte de la dinámica economía y el intercambio comercial entre personas naturales y jurídicas que hacen vida a nivel nacional e internacional.

            Por estas razón, el petro es un proyecto revolucionario, uno de los objetivos del gobierno bolivariano; cuyo alcance implica el incentivo comercial, a través de la generación de ingresos para la reactivación de la economía  y del sistema financiero venezolano y mundial, así como proporcionar herramientas que permitan el intercambio financiero eficiente. 
