	<h4 class="text-center">¿Qué es el Petro? </h4>
		<hr>
		<p class="text-justify"> 
			Es la primera moneda digital y soberana emitida por la Republica Bolivariana de Venezuela. Marca un hito en la historia económica a nivel mundial, al ser la primera moneda digital promovida por un gobierno y además, con respaldo real en riquezas y bienes conmensurables. Es una divisa transable, es decir, cambiable en divisa fiduciaria. Su precio tiene a capacidad y garantia de intercambio inmediato por las riquezas componentes de esta misma.

           El petro, es una herramienta de intercambio financiero a nivel marco, meso y micro económico, que permite la movilidad de recursos en cualquier nivel y la transformación digital de los mismos en su equivalente en cualquier tipo de divisa o criptomoneda, facilitando las transacciones comerciales y financieras entre los interesados.

           El estado venezolano promoverá e incentivara el uso del petro con el objetivo de posicionarlo como opción de inversión mecanismos de ahorro y medio de intercambio con los servicios públicos que presta, así como la venta de sus commodities al exterior, la industria, el comercio y la ciudadanía en general. 
			
