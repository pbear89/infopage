			Es la primera moneda digital y soberana emitida por la Republica Bolivariana de Venezuela .Marca un hito en la historia economica a nivel mundial, al ser la primera moneda digital promovida por un gobierno y ademas,con respaldo real en riquezas y bienes conmensurables.Es una divisa transable, es decir,cambiable en divisa fiduciaria.Su precio tiene a capacidad y garantia de intercambio inmediato por las riquezas componentes de esta misma.

           El petro, es una herramienta de intercambio finaciero a nivel marco, meso y micro economico ,que permite la movilidad de recursos en cualquier nivel y la transformacion digital de los mismos en su equivalente en cualquier tipo de divisa o criptomoneda, facilitando las transacciones comerciales y financieras entre los interesados

           El estado venezolano promovera e incentivara el uso del petro con el objetivo de posicionarlo como opcion de inversion mecanismos de ahorro y medio de intercambio con los servicios publicos que presta,asi como la venta de sus commodities al exterrior , la industria,el comercio y la ciudadania en general 
			
